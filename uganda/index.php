    function allowAlphaNumericSpace(event) {
        const char = String.fromCharCode(event.which);
        const isAlphaNumeric = /^[a-zA-Z0-9\s]$/.test(char);
        if (!isAlphaNumeric) {
            event.preventDefault(); // Block special characters
        }
    }
